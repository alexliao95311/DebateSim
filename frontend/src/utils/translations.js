// React hook for translations (use in components)
import { useState, useEffect } from 'react';
import languagePreferenceService from '../services/languagePreferenceService';

export const useTranslation = () => {
  const [currentLanguage, setCurrentLanguage] = useState(() => {
    try {
      return languagePreferenceService.getCurrentLanguage();
    } catch (e) {
      const saved = localStorage.getItem('language-preference');
      return saved || 'en';
    }
  });

  useEffect(() => {
    // Listen for language changes
    const handleLanguageChange = (lang) => {
      setCurrentLanguage(lang);
    };

    languagePreferenceService.addListener(handleLanguageChange);

    return () => {
      languagePreferenceService.removeListener(handleLanguageChange);
    };
  }, []);

  const t = (key) => {
    const translation = translations[currentLanguage]?.[key] || translations['en'][key] || key;
    return translation;
  };

  return { t, currentLanguage };
};

// Direct translation function for use outside React components
export const translate = (key) => {
  const getLanguage = () => {
    try {
      const languagePreferenceService = require('../services/languagePreferenceService').default;
      return languagePreferenceService.getCurrentLanguage();
    } catch (e) {
      const saved = localStorage.getItem('language-preference');
      return saved || 'en';
    }
  };

  const lang = getLanguage();
  return translations[lang]?.[key] || translations['en'][key] || key;
};

export default translations;

